// Public API — Build Context
// ============================================================

/**
 * Build memdir context for system prompt.
 */
export function buildMemdirContext(entries: MemdirEntry[]): string {
  if (entries.length === 0) return "";

  const sections: Record<string, string[]> = {};
  for (const entry of entries) {
    const title =
      CATEGORY_SECTION_TITLES[entry.category] ??
      (entry.category.charAt(0).toUpperCase() + entry.category.slice(1) + "s");
    if (!sections[title]) sections[title] = [];
    sections[title].push(`- ${entry.content}`);
  }

  const lines = Object.entries(sections).map(
    ([title, items]) => `## ${title}\n${items.join("\n")}`
  );

  return `\n\n## Project Memory\n${lines.join("\n\n")}`;
}
