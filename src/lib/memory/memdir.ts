/**
 * Extract memdir entries from recent conversation.
 * Uses heuristics to detect project-level knowledge.
 * Focused on project-level info: conventions, patterns, decisions, architecture.
 * Limits to 3 entries per call.
 */
export function extractMemdirFromConversation(
  messages: Array<{ role: string; content: string }>
): MemdirEntry[] {
  const results: MemdirEntry[] = [];
  const seenContents: string[] = [];

  const now = new Date().toISOString();

  // Process last 10 messages
  const recent = messages.slice(-10);

  for (const msg of recent) {
    if (!msg.content || msg.content.length < 15) continue;
    const text = msg.content;

    // --- Conventions: coding standards, naming patterns ---
    const conventionPatterns = [
      /(?:we |let'?s |always |make sure to )?(?:follow|use|adopt|stick with) (?:the )?(.+?) (?:naming|naming convention|style|style guide|convention|standard|format|pattern)(?:\.|,|$)/gi,
      /(?:the |our )?(?:naming|coding|style|lint|format) (?:convention|standard|rule|config|pattern) (?:is|should be|are):?\s*(.+?)(?:\.|,|$)/gi,
      /(?:snake_case|camelCase|PascalCase|kebab-case|UPPER_CASE) (?:for|used for|to name) (.+?)(?:\.|,|$)/gi,
    ];

    for (const pat of conventionPatterns) {
      pat.lastIndex = 0;
      const match = pat.exec(text);
      if (match) {
        const content = match[0].trim().replace(/[.,]$/, "");
        if (content.length > 8 && content.length < 300 && !seenContents.some((s) => content.toLowerCase().includes(s.toLowerCase()))) {
          results.push({ category: "convention", content, source: "conversation", updatedAt: now });
          seenContents.push(content);
          if (results.length >= 3) return results;
        }
      }
    }

    // --- Decisions: architectural or technology choices ---
    const decisionPatterns = [
      /(?:the )?(?:architecture|design|system|stack) (?:is|uses|should use|follows) (.+?)(?:\.|,|$)/gi,
      /(?:we |let'?s |i )?(?:decided|chose|went with|are using) (.+?) (?:for|as|because|since)(.+?)(?:\.|,|$)/gi,
    ];

    for (const pat of decisionPatterns) {
      pat.lastIndex = 0;
      const match = pat.exec(text);
      if (match) {
        const content = match[0].trim().replace(/[.,]$/, "");
        if (content.length > 8 && content.length < 300 && !seenContents.some((s) => content.toLowerCase().includes(s.toLowerCase()))) {
          results.push({ category: "decision", content, source: "conversation", updatedAt: now });
          seenContents.push(content);
          if (results.length >= 3) return results;
        }
      }
    }

    // --- Architecture: system design observations ---
    const archPatterns = [
      /(?:the )?(?:api|backend|frontend|client|server|microservice|component|module|service|handler|route) (?:is|are|should be) (?:in|located at|under|at) (.+?)(?:\.|,|$)/gi,
      /(?:the |a )?(?:data|state|config|auth|middleware|layout) (?:layer|flow|pipeline|pattern|architecture) (?:is|uses) (.+?)(?:\.|,|$)/gi,
    ];

    for (const pat of archPatterns) {
      pat.lastIndex = 0;
      const match = pat.exec(text);
      if (match) {
        const content = match[0].trim().replace(/[.,]$/, "");
        if (content.length > 8 && content.length < 300 && !seenContents.some((s) => content.toLowerCase().includes(s.toLowerCase()))) {
          results.push({ category: "architecture", content, source: "conversation", updatedAt: now });
          seenContents.push(content);
          if (results.length >= 3) return results;
        }
      }
    }

    // --- Preferences: user-level project preferences ---
    const prefPatterns = [
      /(?:i |we |the user |user )?(?:prefers?|likes?|wants?) (.+?)(?:\.|,|$)/gi,
    ];

    for (const pat of prefPatterns) {
      pat.lastIndex = 0;
      const match = pat.exec(text);
      if (match) {
        const content = match[0].trim().replace(/[.,]$/, "");
        if (content.length > 8 && content.length < 300 && !seenContents.some((s) => content.toLowerCase().includes(s.toLowerCase()))) {
          results.push({ category: "preference", content, source: "conversation", updatedAt: now });
          seenContents.push(content);
          if (results.length >= 3) return results;
        }
      }
    }

    if (results.length >= 3) return results;
  }

  return results;
}

